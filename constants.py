def get_constants(n: int) -> dict:
    """
    Get SPC constants for subgroup size n.
    For n > 25, uses approximation formulas.

    Returns dict with keys: A2, A3, D3, D4, B3, B4, d2, c4
    """
    if n < 2:
        raise ValueError("Subgroup size n must be >= 2")

    if n <= 25:
        vals = CONSTANTS_TABLE[n]
        return {
            "A2": vals[0], "A3": vals[1], "D3": vals[2], "D4": vals[3],
            "B3": vals[4], "B4": vals[5], "d2": vals[6], "c4": vals[7]
        }
    else:
        # Approximation formulas for n > 25
        import math
        # d2 approximation (from Burr, 1967)
        d2 = math.sqrt(2) * (1 - 1 / (4 * n) - 3 / (32 * n**2) - 15 / (128 * n**3))
        # c4 approximation
        c4 = math.sqrt(2 / (n - 1)) * math.gamma(n / 2) / math.gamma((n - 1) / 2)
        # A2, A3 approximations
        A2 = 3 / (d2 * math.sqrt(n))
        A3 = 3 / (c4 * math.sqrt(n))
        # D3, D4 approximations
        D3 = max(0, 1 - 3 * math.sqrt(1 - c4**2) / c4)
        D4 = 1 + 3 * math.sqrt(1 - c4**2) / c4
        # B3, B4 approximations
        B3 = max(0, 1 - 3 * math.sqrt(1 - c4**2) / c4)
        B4 = 1 + 3 * math.sqrt(1 - c4**2) / c4

        return {
            "A2": A2, "A3": A3, "D3": D3, "D4": D4,
            "B3": B3, "B4": B4, "d2": d2, "c4": c4
        }
