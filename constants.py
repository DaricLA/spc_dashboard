def get_constants(n):
    """返回子组大小 n 对应的常数，n>25 时使用近似公式"""
    if n in CONSTANTS:
        return CONSTANTS[n]
    if n < 2:
        raise ValueError("子组大小必须 ≥ 2")
    # 近似公式（Montgomery）
    d2 = 2 * np.sqrt(n / np.pi)
    c4 = 1 - 1/(4*n) if n > 1 else 0
    A2 = 3 / (d2 * np.sqrt(n))
    A3 = 3 / (c4 * np.sqrt(n))
    D3 = max(0, 1 - 3 * np.sqrt(2/(n-1)))
    D4 = 1 + 3 * np.sqrt(2/(n-1))
    B3 = max(0, 1 - (3/c4)*np.sqrt(1-c4**2)) if c4 > 0 else 0
    B4 = 1 + (3/c4)*np.sqrt(1-c4**2) if c4 > 0 else 0
    return {'A2':A2, 'A3':A3, 'D3':D3, 'D4':D4, 'B3':B3, 'B4':B4, 'd2':d2, 'c4':c4}
